void LCD_16Font(u8 x,u8 y,void *ptr,u8 l,u8 mem)//
{
	u16 i,j,m,k,num,addr;
	u8 uiLen;
	char c1,c2;
	u8 uRow,uCol;

	uiLen = 0;
	i = 0;
	uRow = y*8;
	uCol = x*16;
	
	if((uRow <= LCM_DOT_WIDTH)&&(uCol <= LCM_DOT_HEIGHT))
	{
		if(l != 0)//若l!=0按照设定长度显示
		{
			uiLen = l;
		}
		else
		{
			if(mem == MEM_FLASH)
			{
				while (((const char*)ptr)[uiLen] != 0)	//探测字符串长度
				{
					uiLen++;
				}
			}
			else
			{
				while (((char*)ptr)[uiLen] != 0)	//探测字符串长度
				{
					uiLen++;
				}
			}
		}
		
		while(i < uiLen)
		{
			Kick_Dog();
			if(mem == MEM_FLASH)
			{
				c1 = ((const char*)ptr)[i];
				c2 = ((const char*)ptr)[i+1];
			}
			else
			{
				c1 = ((char*)ptr)[i];
				c2 = ((char*)ptr)[i+1];
			}
			
//			y=i*8;
			addr=i*8+uRow;
			if((c1&0x80)==0)//ASCII字符
			{
				num = 8;			
				if(c1>=0x20)
				{
					for(m=0;m<2;m++)
					{
						SdCmd(0xb0+x+m);
						if(addr<num)
						{
							SdCmd(0x00+addr);
							SdCmd(0x10+0);
						}
						else
						{
							SdCmd(0x00+addr%16);
							SdCmd(0x10+addr/16);
						}
						for(j=0;j<num;j++)
							SdData(ASC_MSK[(c1-0x20)*ASC_CHR_HEIGHT+j+num*m]);
					}
				}
				i++;
			}
			else//中文汉字
			{
				num=16;
				for(j=0;j<sizeof(GB_16)/sizeof(GB_16[0]);j++)	// 判断汉字所在位置
				{
					if((GB_16[j].Index[0] == c1)&&(GB_16[j].Index[1] == c2))
					{
						break;
					}
				}
				for(m=0;m<2;m++)
				{
					if(j < sizeof(GB_16)/sizeof(GB_16[0]))//找到字符串
					{
						SdCmd(0xb0+x+m);
						if(addr<num)
						{
							SdCmd(0x00+addr);
							SdCmd(0x10+0);
						}
						else
						{
							SdCmd(0x00+addr%16);
							SdCmd(0x10+addr/16);
						}
						for(k=0;k<num;k++)
							SdData(GB_16[j].Msk[k+num*m]);
					}
					else
					{
							SdCmd(0xb0+x+m);
							if(addr<num)
							{
								SdCmd(0x00+addr);
								SdCmd(0x10+0);
							}
							else
							{
								SdCmd(0x00+addr%16);
								SdCmd(0x10+addr/16);
							}
							for(k=0;k<num;k++)
								SdData(0xff);
					}
					i++;
				}		
			}
		}
	}
}

//将整形数转换为带小数点的字符串 带符号型
char* LCD_Itoa(char *buf, long i, int len, char dec)
{
	char  *s;
	long  rem;
	long  tmp;
	char  index = 0;

	tmp = i;
	buf[len] = 0;
	s = &buf[len];               

	if (tmp<0)
	{
		tmp = - tmp;
	}
	while( len-- )
	{
		if( dec != 0 )             
		{
			index++;                
			if(index == dec + 1)    
			{
				*--s = '.';
			}
			else                    
			{
				rem = tmp % 10;
				*--s = rem + '0'; 
				tmp /= 10;
			}
		}
		else
		{
			rem = tmp % 10;
			*--s = rem + '0';
			tmp /= 10;
		}
	}

	if (i < 0)
	{
		buf[0]='-';
	}
	return(buf);
}

//将整形数转换为带小数点的字符串 无符号型
char* LCD_Ltoa(char *buf, u32 i, int len, char dec)
{
	char  *s;
	u32  rem;
	u32  tmp;
	char  index = 0;

	tmp = i;
	buf[len] = 0;
	s = &buf[len];                

	while( len-- )
	{
		if( dec != 0 )            
		{
			index++;               
			if(index == dec + 1)    
			{
				*--s = '.';
			}
			else                    
			{
				rem = tmp % 10;
				*--s = rem + '0';  
				tmp /= 10;
			}
		}
		else
		{
			rem = tmp % 10;
			*--s = rem + '0';
			tmp /= 10;
		}
	}

	return(buf);
}

void Scr_Refresh(void)
{
	switch(ucLCD_ScrNum)
	{
		case SCR_000: 
			break;
		case SCR_001:
			Update_Screen_001();		
			break;
		case SCR_002: 
			break;
		case SCR_003: 
			break;
		case SCR_004:
			Update_Screen_004();			
			break;
		default:
			break;
	}
	
	if((ucLCD_ScrNum == SCR_000)||(ucLCD_ScrNum == SCR_001))//如果是待机界面则进行界面定时切换
	{
		ucLCD_ScrUpdate++;
		if(ucLCD_ScrUpdate<5)
		{
			if(ucLCD_ScrNum != SCR_000)
				Dis_Screen_000();
		}
		else
		{
			if(ucLCD_ScrNum != SCR_001)
				Dis_Screen_001();
			if(ucLCD_ScrUpdate>10)
				ucLCD_ScrUpdate = 0;
		}
	}
	
	if(STR_GPRS_A.status > GPRS_CONFM)
		LCD_16Font(0,22,(void*)"号",0,MEM_FLASH);//显示信号	
	else
		LCD_16Font(0,22,(void*)"  ",0,MEM_FLASH);//离线不显示信号
}

