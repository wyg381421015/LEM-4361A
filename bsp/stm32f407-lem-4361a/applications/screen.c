static void screen_thread_entry(void *parameter)//lcd
{
	rt_err_t ret = RT_EOK;
	rt_uint16_t i;
	rt_uint32_t temp;

	lcddev = (rt_device_t)rt_device_find(DEVICE_NAME);//
	
	if(lcddev != RT_NULL)//
	{
		rt_lprintf("[Screen]:lcddev device find sucess!\r\n");
		
		if (rt_device_open(lcddev, 0) == RT_EOK)//
		{
			rt_lprintf("[Screen]:Open lcddev device sucess!\r\n");
		}
	}
	else
	{
		rt_lprintf("[Screen]:Not find device lcddev!\r\n");
		return;
	}
	
	rt_pin_mode(KEY_PIN, PIN_MODE_INPUT_PULLUP);//
//	rt_pin_mode(TOUCH_PIN, PIN_MODE_INPUT_PULLUP);//
	rt_pin_attach_irq(KEY_PIN,PIN_IRQ_MODE_RISING,key_callback,(void*)"lcd backlight on");//
//	rt_pin_attach_irq(TOUCH_PIN,PIN_IRQ_MODE_FALLING,touch_callback,(void*)"lcd backlight on");//
	rt_pin_irq_enable(KEY_PIN,PIN_IRQ_ENABLE);//gpio
//	rt_pin_irq_enable(TOUCH_PIN,PIN_IRQ_ENABLE);//gpio
	
	memset(&stSystem_PasTime.Second,0,sizeof(STR_SYSTEM_TIME));
	
	LCD_DisPlayScreen000();
	
	rt_thread_mdelay(100);
	

	while (1)
	{
//		cmMeter_get_data(EMMETER_HISDATA,&stMeter_HisData.ulMeter_Total);//
				
		LCD_UpdateScreen();//
		
		if(	s_ucLCD_ScrNum != s_ucLCD__PasPageNum)//
			LCD_DisPlayScreen();
		
		s_ucLCD_BKtime++;
		if((s_ucLCD_BKtime>30)&&(s_ucLCD_BKstate != 1))//30 
		{
			s_ucLCD_BKtime = 30;
			s_ucLCD_BKstate = 1;
			s_ucLCD_ScrNum = SCR_001;
			lcddev->control(lcddev,RTGRAPHIC_CTRL_BACKOFF,0);
			rt_lprintf("[Screen]:lcd backlight off!\r\n");
		}
		if(s_ucLCD_TPfinish == 1)// 
		{
			s_ucLCD_TPfinish = 0;
			rt_pin_irq_enable(TOUCH_PIN,PIN_IRQ_ENABLE);
		}
		rt_thread_mdelay(500);
	}
}


